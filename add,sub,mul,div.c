#include<stdio.h>
int main()
{
	int a=8,b=6;
	int add,sub,mul,div;
	add = a + b;
	printf("Addition of %d%d is : %d\n", a, b, add);
	sub = a - b;
	printf("Subraction of %d%d is : %d\n", a, b, sub);
	mul = a * b;
	printf("Multiplication of %d%d is : %d\n", a, b, mul);
	div = a / b;
	printf("Division of %d%d is : %d\n", a, b, div);
	return 0;
}
